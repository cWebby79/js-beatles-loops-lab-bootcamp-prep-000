function theBeatlesPlay(musicians,instruments) {
  var newArray = [];
  for (let i = 0; i < musicians.length; i++) {
    newArray.push(musicians[i] + ' plays '+ instruments[i]);
  }
  return newArray;
}

function johnLennonFacts(facts) {
  var counter;
  while (counter < facts.length) {
    facts.push[counter]('!!!');
    counter++;
  }
  return facts;
}